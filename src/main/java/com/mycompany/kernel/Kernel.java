        Vector<String> nombres = new Vector<String>();
        for (int x = 0; x < nombreLista.getSize(); x++){
            nombres.add(nombreLista.getElementAt(x));
        }

        conte.reloj++;
        String relojS = "" + conte.reloj;
           tiempo.setText(relojS);
           

